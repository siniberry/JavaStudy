import java.util. Scanner;
import java.io. IOException;

class Calculate
{
	int a,b;
	char c;

	void input()throws IOException
	{
		Scanner sc = new Scanner(System.in);
		System.out.print("임의의 두 정수 입력(공백 구분) : ");
		a = sc.nextInt();
		b = sc.nextInt();
		System.out.print("임의의 연산자 입력 (+ - * /)   : ");
		c=(char)System.in.read();
	}

	int cal()
	{
		int result=0;
		/*
		if (c=='+')
		{
			result = a + b;
			return result;// 이렇게 하던가 아니면 밖에서 리턴해줘야함
	    }		
		else if (c=='-')
			result = a - b;

		else if (c=='*')
			result = a * b;

		else if (c == '/')
			result = a / b;

		else
		return result;
		*/



		switch (c)
		{
		case  '+' : result = a + b;break;
		case  '-' : result = a - b;break;
		case  '*' : result = a * b;break;
		case  '/' : result = a / b;break;
		// default를 쓰고 싶으면 default : result =-9999 이런 식으로 하는 게 바람직 함
		
		}

		return result;
	}


	void print(int sum)
	{
		System.out.printf(">> %d %c %d = %d",a,c,b,sum);
	}
}
public class Test075
{
	public static void main(String[]args)throws IOException
	{
		Calculate ob = new Calculate();

		ob.input();
		int a =ob.cal();
		ob.print(a);
	}
}

